export function getGatewaysByRegion(region) {
  const map = {
    BR: BrazilGateways,
    PH: PhilippinesGateways,
    IN: IndiaGateways,
    VN: VietnamGateways,
    ID: IndonesiaGateways,
    OTHER: OtherGateways
  }
  return map[region] || ALL_GATEWAYS
}

export function getBanksByRegion(region) {
  const map = {
    BR: [],
    PH: PhilippinesBanks,
    IN: IndiaBanks,
    VN: VietnamBanks,
    ID: IndonesiaBanks
  }
  return map[region] || []
}

export function isValidGateway(code) {
  return ALL_GATEWAYS.includes(code)
}

export function isValidBank(code) {
  return ALL_BANKS.includes(code)
}
